// 经典兔子繁衍问题，斐波那契数列问题
// 一对兔子，从出生后第3个月起每个月都生一对兔子。小兔子长到第3个月后每个月又生一对兔子。
//假如兔子都不死，请问第1个月出生的一对兔子，至少需要繁衍到第几个月时兔子总数才可以达到N对？

#include <stdio.h>
int main(void)
{
    int n;
    scanf("%d", &n);
    int a1 = 1, a2 = 1, a3, m = 2;
    if (n > 1)
    {
        while (a3 < n)
        {
            m++;
            a3 = a1 + a2;
            a1 = a2;
            a2 = a3;
        }
        printf("%d", m);
    }
    else
        printf("%d", 1);
}